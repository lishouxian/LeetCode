# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class Solution:
    def mergeKLists(self, lists):
        def merge(left, right):
            if right - left <= 2:
                node1 = lists[left]
                if right - left == 2:
                    node2 = lists[left + 1]
                else:
                    node2 = None
            else:
                mid = (left + right) // 2
                node1 = merge(left, mid)
                node2 = merge(mid, right)
            res = ListNode()
            head = res
            while node1 is not None or node2 is not None:
                if node1 is None:
                    head.next = node2
                    break
                elif node2 is None:
                    head.next = node1
                    break
                elif node1.val <  node2.val:
                    head.next = node1
                    node1 = node1.next
                else:
                    head.next = node2
                    node2 = node2.next
                head = head.next
            return res.next
        if not lists: return None
        return merge(0, len(lists))

