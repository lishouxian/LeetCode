class Solution:
    def simplifyPath(self, path: str) -> str:
        res = []
        temp = path.split('/')
        print(temp)
        for i in temp:
            if i == "..":
                if res: res.pop()
            elif i != '.' and i:
                res.append(i)
        print(res)
        return '/' + '/'.join(res)


